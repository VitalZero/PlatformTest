        public void Load(ContentManager content)
        {
            texture = content.Load<Texture2D>("MARIOTILESSIMPLE");
        }

        public void Draw(SpriteBatch spriteBatch)
        {
            for(int y = 0; y < mapHeight; ++y)
            {
                for(int x = 0; x < mapWidth; ++x)
                {
                    int tileTexture = map[x + mapWidth * y];

                    if (tileTexture >= 0)
                    {
                        int dx = (tileTexture % tileSize) * tileSize;
                        int dy = (tileTexture / tileSize) * tileSize;

                        spriteBatch.Draw(
                            texture,
                            new Vector2(x * tileSize, y * tileSize),
                            new Rectangle(dx, dy, tileSize, tileSize),
                            Color.White
                            );
                    }
                }
            }
        }
